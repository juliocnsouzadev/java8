package br.com.juliocnsouza.java8.stream;

/**
 *
 * @author juliocnsouzadev
 */
public class Curso {

    private int alunos;

    private String nome;

    public Curso( int alunos , String nome ) {
        this.alunos = alunos;
        this.nome = nome;
    }

    public int getAlunos() {
        return alunos;
    }

    public String getNome() {
        return nome;
    }

}
